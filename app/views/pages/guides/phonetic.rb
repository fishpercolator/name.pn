      # #{title 'How to write a phonetic spelling'}

      Spelling and pronunciation are not closely linked in English. Without someone teaching you, how would you know the word "**cough**" is pronounced "**COFF**" but the word "**through**" is pronounced "**THROO**"?

      This is especially true of names! Someone without help is never going to be able to work out that the surname "**Featherstonhaugh**" is pronounced "**FAN-shaw**".

      A phonetic spelling is a way of spelling your name using common sequences of letters in English, which makes it easier for English speakers to understand how to say it without hearing it. Of course, name.pn also allows you to record your name in your own voice, but it's great to have both on your page in case someone is not able to listen to the recording at the time they view your page.

      ## The elements of a phonetic spelling

      There are three things to consider when writing a phonetic spelling of your name:

      1. Syllables
      2. Spelling
      3. Stress
      
      In this guide, we'll explore these three elements one by one.

      ## Syllables

      Syllables are the rhythmic sounds of your name. Every word has at least one syllable, but it may have many more.

      A syllable usually has a vowel sound (or a pair of vowel sounds) in the middle and maybe consonants either side.

      One way to imagine your name is in a song. The syllables would form the "notes" of the song. (If you can't think of a tune, try putting the name in the "Happy Birthday" song!) For example, try singing these names:

      Christopher
      : 3 syllables: **chris-to-pher**

      Anna
      : 2 syllables: **a-nna**
      
      Paige
      : 1 syllable: **paige** (in the "Happy Birthday" song you might sing the vowel twice - "pai-aige" - to make two notes but that's not how you say it when speaking.)

      In a phonetic spelling we show multiple syllables by separating them with the hyphen (-).

      ## Spelling

      A phonetic spelling of a syllable reduces it to more "obvious" spellings for speakers of English.

      For example, if your name is **John**, you might write "**jon**" so it's clear the _h_ is silent.

      Other examples (note, many of these have more than one possible pronunciation - you might pronounce your name differently to these):

      * **Phoebe** → **fee-bee**
      * **Rhys** → **rees**
      * **Niamh** → **neev**
      * **Geoffrey** → **jef-ree**
      * **Hugh** → **hyoo**
      
      ### The schwa (ə)
      
      A very common vowel sound in names is called the [schwa](https://en.wikipedia.org/wiki/Schwa). It's a kind of neutral vowel that doesn't have a standard spelling.

      In fact, in many cases it's written without any letters at all. Consider the word **rhythm** - there are two syllables here but only one of them has a vowel in the spelling - the _y_. The vowel in the second syllable appears between _th_ and _m_ but it has no spelling. This is a schwa - and is sometimes written using its [IPA](https://en.wikipedia.org/wiki/International_Phonetic_Alphabet) symbol, like this: **rih-thəm**.

      I'd recommend when writing a phonetic spelling of your name, you spell the syllable without a vowel, or use "uh" as an approximate spelling of the schwa. If you want to be really explicit, name.pn also lets you add the IPA spelling of your name alongside the phonetic spelling.

      Some examples of schwas in names:

      * **Michael** → **my-kl** (or **my-kuhl**)
      * **Stephen** → **stee-vn** (or **stee-vuhn**)
      * **Theresa** → **tuh-ree-suh**

      ### The letter r
      
      Be careful when including the letter _r_ in your phonetic spelling, especially if you speak a "non-rhotic" variety of English, such as that spoken in most parts of England.

      Speakers of these varieties of English tend not to pronounce the letter _r_ unless it appears at the start of a syllable, but this is not true of rhotic varieties such as those spoken in Scotland and most of the USA.

      For example, the name "**Tyler**" to speakers of non-rhotic English sounds more like "**ty-luh**", whereas rhotic speakers will sound out the letter _r_ at the end of the name.

      If you are from England or another area with a non-rhotic variety of English, you might be tempted to write the letter _r_ in your phonetic spellings even though you don't pronounce it. For example, the name "Palmer" you might write "_par-mer_" because when you read this spelling, you don't pronounce the _r_. But be aware that speakers of rhotic varieties will think to pronounce every _r_. Instead, it might be better to write something like "**pah-muh**" so it is clear you do not pronounce the letter _r_.

      As a result of this distinction, some common names are pronounced quite differently between rhotic and non-rhotic areas. The name "**Eleanor**" is typically pronounced "**el-uh-nuh**" in non-rhotic areas (such as England) and "**el-uh-nor**" (pronouncing both the _o_ and the _r_) in rhotic areas (such as the USA).

      ## Stress

      The last part to understand is _stress_. In English, words of more than one syllable always have a **primary stress** - this is one syllable of the word that is pronounced with a little more force than the other syllables. (Long words sometimes have a secondary stress too.) This is part of how people listening can tell where one word stops and the next starts.

      Try sounding out this famous sentence:

      > It is a truth universally acknowledged, that a single man in possession of a good fortune, must be in want of a wife.
      
      There are five words in this sentence with more than one syllable. In each case, one of the syllables is said with a little more force than the others:

      > It is a truth un-i-**VER**-sa-lly ack-**NOW**-ledged, that a **SIN**-gle man in poss-**ES**-sion of a good **FOR**-tune, must be in want of a wife.

      The same is true of names. As some examples:

      * Jonathan → **JON**-a-than
      * Samantha → sam-**AN**-tha
      * Alexander → al-ex-**AN**-der
      * Marie → ma-**RIE** (although in French is it often pronounced **MA**-rie, like the word "marry")

      If your name has more than one syllable, try saying it out loud a few times and listen for that stressed syllable. Try stressing other syllables to see how it sounds - and how it sounds "wrong" to your ears - this might help you identify the stressed syllable.

      In phonetic spellings, we write the stressed syllable using CAPITAL LETTERS. If the word has only one syllable, it's conventional to write it in CAPITAL LETTERS too but it's not really important.

      ## Putting it all together

      Now you know all the parts, all that's left is to combine them to get a great phonetic spelling of your own name. Here are some examples to get you on your way:

      * (My name) Quinn Daley → **KWIN DAY-lee**
      * Barack Obama → **buh-RAHK oh-BAH-muh**
      * Marsha P Johnson → **MAR-shuh PEE JON-sun**
      * Rabindranath Tagore → **ruh-BIN-druh-naht ta-GOR**
      * Ada Lovelace → **AY-duh LUV-layce**
      * Eleanor Roosevelt → **EL-in-or ROSE-uh-velt**

      ## Learning more

      [The Wikipedia article on what they call "pronunciation respelling"](https://en.wikipedia.org/wiki/Pronunciation_respelling_for_English) will guide you to more information about this way of writing English pronunciations.

      [Learn more about the IPA](https://en.wikipedia.org/wiki/International_Phonetic_Alphabet) for the other way of spelling the pronunciation of your name.

      If you find phonetics fun and want to learn more with an engaging online guidebook and community, check out [Funetics](https://www.funetics.com/), from professional accent coach and phonetics enthusiast Luke Nicholson.
